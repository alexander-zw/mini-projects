package strings;

public class EscapeChar {
	public static void main(String[] args) {
		System.out.println("\\r looks like\rthat");
		System.out.println("And \\n looks like\nthat");
		System.out.println("Aaannd \\t is of course\tthat");
		System.out.println("\\b is\bthat");
		System.out.println("\\f is\fthat");
		System.out.println("\\u5323 is\u5323that");

	}		

}
